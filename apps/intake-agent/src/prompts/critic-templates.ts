/**
 * Critic prompt for task generation output.
 */
export const TASK_CRITIC_SYSTEM_PROMPT = `You are a project management and software architecture expert.
Your job is to validate AI-generated tasks for software development projects.

Evaluate tasks for:
1. **Completeness** - Does each task have clear title, description, dependencies, and acceptance criteria?
2. **Scope** - Are tasks appropriately sized (not too large or too small)?
3. **Dependencies** - Are task dependencies logical and correctly ordered?
4. **Clarity** - Can a developer understand what to do without ambiguity?
5. **Testability** - Does each task have a clear definition of done?
6. **Agent Assignment** - Is the assigned agent appropriate for the task type?

Output your evaluation as JSON with the standard format.`;

/**
 * Critic prompt for code generation output.
 */
export const CODE_CRITIC_SYSTEM_PROMPT = `You are a senior software engineer and code reviewer.
Your job is to validate AI-generated code for production readiness.

Evaluate code for:
1. **Correctness** - Does the code do what it's supposed to?
2. **Security** - Are there any security vulnerabilities?
3. **Performance** - Are there obvious performance issues?
4. **Error Handling** - Are errors handled appropriately?
5. **Maintainability** - Is the code readable and maintainable?
6. **Testing** - Is the code testable? Are tests included if requested?

Output your evaluation as JSON with the standard format.`;

/**
 * Critic prompt for documentation output.
 */
export const DOCS_CRITIC_SYSTEM_PROMPT = `You are a technical writer and documentation expert.
Your job is to validate AI-generated documentation for clarity and completeness.

Evaluate documentation for:
1. **Accuracy** - Is the information technically correct?
2. **Completeness** - Are all necessary topics covered?
3. **Clarity** - Can the target audience understand it?
4. **Structure** - Is it well-organized and easy to navigate?
5. **Examples** - Are there appropriate examples where needed?
6. **Consistency** - Is terminology and style consistent?

Output your evaluation as JSON with the standard format.`;

// =============================================================================
// Helper Functions
// =============================================================================

/**
 * Get the appropriate system prompt for a given content type.
 */
export function getCriticSystemPrompt(contentType?: string): string {
  switch (contentType) {
    case 'tasks':
      return TASK_CRITIC_SYSTEM_PROMPT;
    case 'code':
      return CODE_CRITIC_SYSTEM_PROMPT;
    case 'docs':
    case 'documentation':
      return DOCS_CRITIC_SYSTEM_PROMPT;
    default:
      return CRITIC_SYSTEM_PROMPT;
  }
}

/**
 * Build a critic user prompt from context.
 */
export function buildCriticPrompt(options: {
  content: string;
  context?: string;
  originalPrompt?: string;
  criteria?: string;
}): string {
  return CRITIC_USER_TEMPLATE({
    content: options.content,
    context: options.context,
    original_prompt: options.originalPrompt,
    criteria: options.criteria,
  });
}

/**
 * Build a refiner user prompt from critic results.
 */
export function buildRefinerPrompt(options: {
  content: string;
  originalPrompt: string;
  generator: string;
  critic: string;
  approved: boolean;
  confidence: number;
  issues: Array<{ severity: string; location: string; description: string; suggestion: string }>;
  suggestions: string[];
  reasoning: string;
}): string {
  return REFINER_USER_TEMPLATE(options);
}
