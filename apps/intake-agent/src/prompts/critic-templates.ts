// User Prompts (Templates)
// =============================================================================

/**
 * Template for the critic's user prompt.
 */
export const CRITIC_USER_TEMPLATE = Handlebars.compile(`Evaluate the following content that was generated by another AI model.

## Context
{{#if context}}
{{{context}}}
{{else}}
The content was generated in response to a user request and needs validation.
{{/if}}

## Original Request
{{#if original_prompt}}
{{{original_prompt}}}
{{/if}}

## Content to Evaluate
\`\`\`
{{{content}}}
\`\`\`

## Evaluation Criteria
{{#if criteria}}
{{{criteria}}}
{{else}}
Evaluate for completeness, correctness, quality, edge cases, and best practices.
{{/if}}

Provide your evaluation as a JSON object with approved (boolean), confidence (0-1), issues (array), suggestions (array), and reasoning (string).`);

/**
 * Template for the refiner's user prompt.
 */
export const REFINER_USER_TEMPLATE = Handlebars.compile(`Please refine the following content based on the critique provided.

## Original Request
{{{original_prompt}}}

## Original Content (Generated by {{generator}})
\`\`\`
{{{content}}}
\`\`\`

## Critique from {{critic}}
**Approved:** {{approved}}
**Confidence:** {{confidence}}

### Issues Found:
{{#each issues}}
- **{{severity}}** at "{{location}}": {{description}}
  - Suggestion: {{suggestion}}
{{/each}}

### General Suggestions:
{{#each suggestions}}
- {{this}}
{{/each}}

### Critic's Reasoning:
{{reasoning}}

---

Please provide an improved version that addresses the issues above.
Maintain the same format as the original content.
Focus on fixing critical and major issues first.`);

